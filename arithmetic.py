# PYTHON PROGRAM TO DO  ALL ARITHMETIC OPERATIONS

number1 = int(input('Enter the 1st number'))
number2 = int(input('Enter the 2nd number'))

#Addition
sum = number1 + number2
print('The sum of {} and {} is {}'.format(number1,number2,sum))

#Subtraction
difference = number1 - number2
print('The difference of {} and {} is {}'.format(number1,number2,difference))

#Multiplication
product = number1 * number2
print('The product of {} and {} is {}'.format(number1,number2,product))

#Division
Division = number1 / number2
print('The divison of {} and {} is {}'.format(number1,number2,Division))

#Remainder
Remainder = number1 % number2
print('The remainder of {} and {} is {}'.format(number1,number2,Remainder))
